package org.foxesworld.engine.server;

public class ServerAttributes {

    @SuppressWarnings("unused")
    private int id, port;
    @SuppressWarnings("unused")
    private String serverName, serverVersion, mainClass, jreVersion, forgeVersion, forgeGroup, serverImage, serverDescription, mcpVersion, client, host, ignoreDirs;

    @SuppressWarnings("unused")
    public int getId() {
        return id;
    }

    @SuppressWarnings("unused")
    public String getServerName() {
        return serverName;
    }

    @SuppressWarnings("unused")
    public String getServerVersion() {
        return serverVersion;
    }

    @SuppressWarnings("unused")
    public String getMainClass() {
        return mainClass;
    }

    @SuppressWarnings("unused")
    public String getJreVersion() {
        return jreVersion;
    }

    @SuppressWarnings("unused")
    public String getForgeVersion() {
        return forgeVersion;
    }

    @SuppressWarnings("unused")
    public String getMcpVersion() {
        return mcpVersion;
    }

    @SuppressWarnings("unused")
    public String getForgeGroup() {
        return forgeGroup;
    }

    @SuppressWarnings("unused")
    public String getClient() {
        return client;
    }

    @SuppressWarnings("unused")
    public String getHost() {
        return host;
    }

    @SuppressWarnings("unused")
    public int getPort() {
        return port;
    }

    @SuppressWarnings("unused")
    public String getServerImage() {
        return serverImage;
    }

    @SuppressWarnings("unused")
    public String getServerDescription() {
        return serverDescription;
    }

    @SuppressWarnings("unused")
    public String getIgnoreDirs() {
        return ignoreDirs;
    }
}

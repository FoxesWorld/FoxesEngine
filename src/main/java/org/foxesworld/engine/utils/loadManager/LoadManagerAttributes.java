package org.foxesworld.engine.utils.loadManager;

public class LoadManagerAttributes {
    private String path;
    private int rows, cols,delay, x, y, width, height;

    public String getPath() {
        return path;
    }

    public int getRows() {
        return rows;
    }

    public int getCols() {
        return cols;
    }

    public int getDelay() {
        return delay;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}

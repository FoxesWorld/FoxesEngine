package org.foxesworld.engine.utils.loadManager;

public class LoadManagerAttributes {
    private String path;
    private int rows, cols,delay;
    private Bounds bounds;

    public String getPath() {
        return path;
    }

    public int getRows() {
        return rows;
    }

    public int getCols() {
        return cols;
    }

    public int getDelay() {
        return delay;
    }

    public Bounds getBounds() {
        return bounds;
    }
}
